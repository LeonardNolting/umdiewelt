function removeKeys(obj, keys, deep = true) {
	if (typeof obj !== 'object' || obj === null) return;

	for (const key in obj) {
		if (obj.hasOwnProperty(key)) {
			if (keys.includes(key)) {
				delete obj[key];
			} else {
				if (deep) removeKeys(obj[key], keys, deep);
			}
		}
	}
}

